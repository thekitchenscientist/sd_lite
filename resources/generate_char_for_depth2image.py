import PIL
from PIL import ImageFont
from PIL import Image
from PIL import ImageDraw

width_x = 512
width_y = 512
# font file needs to be in same folder as this script
FONT_FACE = "msyhbd.ttc"

def make_image(width_x,width_y,phrase,i=None):
    if i is not None:
        character = phrase[i:i+1]
        i=str(i)
    else:
        character=phrase
        i=""
    font = ImageFont.truetype(FONT_FACE,400)
    img=Image.new("1", (width_x,width_y),(0))
    draw = ImageDraw.Draw(img)
    draw.text((50, -20),character,(1),font=font)
    img.save('../inputs/'+phrase+i+'_character.png','PNG')


for phrase in vocab_list:
    if len(phrase) ==1:
        make_image(width_x,width_y,phrase)
    else:
        for i in range(0,len(phrase)):
            make_image(width_x,width_y,phrase,i)
        
    